    public void tampilkanData() {

        String[] kolom = {"Menu", "Kuantitas", "Harga"};

        ArrayList<Pesan> list = new Pesan().getAll();
        Object rowData[] = new Object[3];

        tblMenu.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getBarang().getMakanan();
            rowData[1] = list.get(i).getJumlah();
            rowData[2] = list.get(i).getHarga();
            ((DefaultTableModel) tblMenu.getModel()).addRow(rowData);
        }
    }

    public void cari(String keyword) {
        String[] kolom = {"Menu", "Kuantitas", "Harga"};

        ArrayList<Pesan> list = new Pesan().search(keyword);
        Object rowData[] = new Object[3];

        tblMenu.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Pesan pesan : list) {
            rowData[0] = pesan.getBarang().getMakanan();
            rowData[1] = pesan.getJumlah();
            rowData[2] = pesan.getHarga();
            ((DefaultTableModel) tblMenu.getModel()).addRow(rowData);
        }
    }

