    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Pesan pesan = new Pesan();
        int kasir = new Kasir().getById(1).getIdkasir();
        pesan.getKasir().setIdkasir(kasir);
        pesan.getCustomer().setNama(txtNama.getText());
        pesan.getCustomer().setIdcustomer(2);
        Barang barang = new Barang().getById(Integer.parseInt(txtMenu.getText()));
        pesan.setBarang(barang);
        pesan.setJumlah(Integer.parseInt(txtJumlah.getText()));
        pesan.save();
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

